#include "ft_printf.h"
#include "libft.h"

int	print_env(t_list *env)
{
	t_list	*tmp;

	tmp = env;
	while (tmp != NULL)
	{
		ft_printf("%s\n", tmp->content);
		tmp = tmp->next;
	}
	return (EXIT_SUCCESS);
}
